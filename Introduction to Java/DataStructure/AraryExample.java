package DataStructure;

public class AraryExample {
    public static void main(String[] args) {
        // Following code declares an int array
        int myArr[] = { 1, 5, 993, 35 };

        // following code interates through array elements and print them one by one
        for (int b = 0; b < myArr.length; b++) {
            System.out.println(myArr[b]);
        }
    }
}
