package Operator;

public class Relational {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        if (a == b) {
            System.out.println("a == b");
        }

        if (a != b) {
            System.out.println("a != b");
        }

        if (a < b) {
            System.out.println("a < b");
        }

        if (a > b) {
            System.out.println("a > b");
        }
    }
}
